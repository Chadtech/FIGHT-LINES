////////////////////////////////////////////////////////////////
// TYPES //
////////////////////////////////////////////////////////////////

#[derive(Copy, Clone)]
pub struct Session {
    api_url: &'static str,
}

////////////////////////////////////////////////////////////////
// INIT //
////////////////////////////////////////////////////////////////

pub fn init_dev() -> Session {
    Session {
        api_url: DEV_API_URL,
    }
}

static DEV_API_URL: &str = "http://localhost:2943";

////////////////////////////////////////////////////////////////
// API //
////////////////////////////////////////////////////////////////

impl Session {
    pub fn url(self, path: &str) -> String {
        let mut buf: String = String::new();

        buf.push_str(self.api_url);
        buf.push_str(path);

        buf
    }
}
