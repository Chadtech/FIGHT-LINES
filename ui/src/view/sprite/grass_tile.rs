use crate::session::Session;
use crate::view::image;
use crate::view::image::Image;

////////////////////////////////////////////////////////////////
// Types //
////////////////////////////////////////////////////////////////

pub enum Model {
    Model,
}

////////////////////////////////////////////////////////////////
// CONSTS //
////////////////////////////////////////////////////////////////

static FILE_NAME: &str = "grass_square";

////////////////////////////////////////////////////////////////
// API //
////////////////////////////////////////////////////////////////

impl Model {
    pub fn init() -> Model {
        Model::Model
    }

    pub fn to_img(self, session: Session) -> Image {
        Image::from_source(session.asset_url(FILE_NAME)).with_size(image::Size {
            width: 48,
            height: 48,
        })
    }
}
